public class Punto{
    double coordenadaX;
    double coordenadaY;
    public Punto(double coordenadaX, double coordenadaY){
           this.coordenadaX = coordenadaX; 
           this.coordenadaY = coordenadaY;
    }
    public double getCoordenadaX(){
        return coordenadaX;
    }
    public double getCoordenadaY(){
        return coordenadaY;
    }
}
